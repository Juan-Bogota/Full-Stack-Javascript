// OPerador ternario

//(condicional) ? true:false


let num = 2;

(num % 2 === 0) ? console.log(`${num} es par`) : console.log(`${num} es impar`);

// Operador ternario en bloque

(num % 2 === 0) ?
(
    console.log(`${num} es par`),
    console.log(`${num} es par`)
)
 : 
(
    console.log(`${num} es impar`),
    console.log(`${num} es impar`)
)
 